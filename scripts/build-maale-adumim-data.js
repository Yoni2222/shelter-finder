function parseRawData() {
  const lines = RAW_DATA.trim().split('\n');
  const entries = [];

  for (const line of lines) {
    const [neighborhood, rawAddress] = line.split('|');
    const address = rawAddress.trim();

    entries.push({
      address,
      city: CITY,
      neighborhood: neighborhood.trim(),
    });
  }

  return entries;
}

async function geocode(address, city, retries = 3) {
  const query = `${address}, ${city}, ישראל`;
  const url = 'https://maps.googleapis.com/maps/api/geocode/json?' + new URLSearchParams({
    address: query,
    key: GOOGLE_API_KEY,
    language: 'he',
    region: 'il',
  });

  for (let attempt = 1; attempt <= retries; attempt++) {
    try {
      const res = await fetch(url, { timeout: 10000 });
      if (!res.ok) {
        if (attempt < retries) {
          console.warn(`    HTTP ${res.status}, retrying (${attempt}/${retries})...`);
          await new Promise(r => setTimeout(r, 1000 * attempt));
          continue;
        }
        return null;
      }
      const data = await res.json();

      if (data.status === 'OVER_QUERY_LIMIT') {
        console.warn(`    RATE LIMITED, waiting ${2 * attempt}s and retrying (${attempt}/${retries})...`);
        await new Promise(r => setTimeout(r, 2000 * attempt));
        continue;
      }

      if (data.status !== 'OK' || !data.results || !data.results[0]) return null;
      const loc = data.results[0].geometry.location;
      const addressEn = data.results[0].formatted_address || '';
      return { lat: loc.lat, lon: loc.lng, addressEn };
    } catch (err) {
      if (attempt < retries) {
        console.warn(`    Error: ${err.message}, retrying (${attempt}/${retries})...`);
        await new Promise(r => setTimeout(r, 1000 * attempt));
        continue;
      }
      return null;
    }
  }
  return null;
}

function inBounds(lat, lon) {
  return lat >= 31.74 && lat <= 31.79 && lon >= 35.27 && lon <= 35.34;
}

async function main() {
  if (!GOOGLE_API_KEY) {
    console.error('ERROR: No Google API key found. Set GOOGLE_API_KEY or GOOGLE_MAPS_API_KEY env var, or add to .env.');
    process.exit(1);
  }

  // Step 1: Parse and create input JSON
  const entries = parseRawData();
  const inputPath = path.join(__dirname, `${CITY_SLUG}-input.json`);
  fs.writeFileSync(inputPath, JSON.stringify(entries, null, 2), 'utf8');
  console.log(`Created ${inputPath} with ${entries.length} entries`);

  // Step 2: Geocode
  console.log(`\nGeocoding ${entries.length} addresses...`);
  const shelters = [];
  const failures = [];
  const outOfBounds = [];
  let geocodeOk = 0;

  for (let i = 0; i < entries.length; i++) {
    const e = entries[i];
    const coords = await geocode(e.address, e.city);

    if (!coords) {
      failures.push({ index: i, address: e.address, neighborhood: e.neighborhood });
      console.warn(`  [${i+1}/${entries.length}] FAIL - no coords for: ${e.address}`);
    } else if (!inBounds(coords.lat, coords.lon)) {
      outOfBounds.push({ index: i, address: e.address, neighborhood: e.neighborhood, lat: coords.lat, lon: coords.lon });
      console.warn(`  [${i+1}/${entries.length}] OUT OF BOUNDS: ${e.address} -> (${coords.lat}, ${coords.lon})`);
      // Still include it but flag it
      shelters.push({
        id: `${CITY}-${shelters.length + 1}`,
        lat: coords.lat,
        lon: coords.lon,
        name: `מקלט - ${e.address}`,
        address: e.address,
        city: CITY,
        neighborhood: e.neighborhood,
        type: 'מקלט ציבורי',
        source: 'gov',
        category: 'public',
        addressEn: coords.addressEn || '',
        _outOfBounds: true,
      });
      geocodeOk++;
    } else {
      geocodeOk++;
      shelters.push({
        id: `${CITY}-${shelters.length + 1}`,
        lat: coords.lat,
        lon: coords.lon,
        name: `מקלט - ${e.address}`,
        address: e.address,
        city: CITY,
        neighborhood: e.neighborhood,
        type: 'מקלט ציבורי',
        source: 'gov',
        category: 'public',
        addressEn: coords.addressEn || '',
      });
    }

    if ((i + 1) % 20 === 0) {
      console.log(`  Geocoded ${i+1}/${entries.length}...`);
    }

    // 200ms delay between requests
    await new Promise(r => setTimeout(r, 200));
  }

  // Step 3: Write outputs
  // Remove _outOfBounds flag for final output
  const cleanShelters = shelters.map(s => {
    const { _outOfBounds, ...rest } = s;
    return rest;
  });

  const outputPath = path.join(__dirname, `${CITY_SLUG}-output.json`);
  fs.writeFileSync(outputPath, JSON.stringify(cleanShelters, null, 2), 'utf8');
  console.log(`\nSaved ${outputPath}`);

  const dataPath = path.join(__dirname, '..', 'data', `${CITY_SLUG}-shelters.json`);
  fs.mkdirSync(path.dirname(dataPath), { recursive: true });
  fs.writeFileSync(dataPath, JSON.stringify(cleanShelters, null, 2), 'utf8');
  console.log(`Saved ${dataPath}`);

  // Step 4: Stats
  console.log(`\n=== STATS ===`);
  console.log(`Total input entries: ${entries.length}`);
  console.log(`Geocoded OK: ${geocodeOk}`);
  console.log(`Failed: ${failures.length}`);
  console.log(`Out of bounds: ${outOfBounds.length}`);
  console.log(`Final shelter count: ${cleanShelters.length}`);

  if (failures.length > 0) {
    console.log(`\nFailed addresses:`);
    failures.forEach(f => console.log(`  - [${f.neighborhood}] ${f.address}`));
  }
  if (outOfBounds.length > 0) {
    console.log(`\nOut-of-bounds (included but check manually):`);
    outOfBounds.forEach(o => console.log(`  - [${o.neighborhood}] ${o.address} -> (${o.lat}, ${o.lon})`));
  }
}

main().catch(e => { console.error(e.message); process.exit(1); });
