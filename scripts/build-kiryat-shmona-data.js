const CITY = 'קריית שמונה';

async function geocode(address) {
  const url = `https://maps.googleapis.com/maps/api/geocode/json?address=${encodeURIComponent(address)}&key=${API_KEY}`;
  const r = await fetch(url);
  const data = await r.json();
  if (data.results && data.results.length > 0) {
    const loc = data.results[0].geometry.location;
    const type = data.results[0].geometry.location_type;
    // Skip APPROXIMATE results (city-level, useless)
    if (type === 'APPROXIMATE') return null;
    return { lat: loc.lat, lon: loc.lng, formatted: data.results[0].formatted_address, type };
  }
  return null;
}

async function main() {
  const shelters = [];
  let success = 0, fail = 0;

  for (const [num, street, houseNum] of RAW) {
    const addrLocal = houseNum ? `${street} ${houseNum}` : street;
    const fullAddr = `${addrLocal}, ${CITY}, ישראל`;
    const geo = await geocode(fullAddr);
    if (geo) {
      shelters.push({
        id: `kiryat_shmona_${num}`,
        lat: geo.lat,
        lon: geo.lon,
        name: `מקלט ${num}`,
        address: houseNum ? `${street} ${houseNum}, ${CITY}` : `${street}, ${CITY}`,
        city: CITY,
        capacity: '',
        type: 'מקלט ציבורי',
        source: 'gov',
        category: 'public',
        addressEn: geo.formatted,
      });
      success++;
      console.log(`✓ ${num}: ${addrLocal} → ${geo.lat}, ${geo.lon} (${geo.type})`);
    } else {
      fail++;
      console.log(`✗ ${num}: ${addrLocal} → FAILED`);
    }
    // Rate limit: 50ms between requests
    await new Promise(r => setTimeout(r, 50));
  }

  console.log(`\nTotal: ${success} geocoded, ${fail} failed out of ${RAW.length}`);

  const outPath = path.join(__dirname, '..', 'data', 'kiryat-shmona-shelters.json');
  fs.writeFileSync(outPath, JSON.stringify(shelters, null, 2), 'utf8');
  console.log(`Written to ${outPath}`);
}

main().catch(console.error);
